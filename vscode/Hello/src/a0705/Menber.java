public class Menber {
    String name;
    String id;
    String password;
    int age;

    public Menber(String n, String i) {
        name = n;
        id = i;
    }

    public Menber(String name, String id, String password, int age) {
        this.name = name;
        this.id = id;
        this.password = password;
        this.age = age;
    }

}
